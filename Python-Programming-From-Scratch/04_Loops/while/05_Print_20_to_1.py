# ============================================================
# Program 05 : Print Numbers from 20 to 1
# ============================================================

# Problem Statement:
# Write a Python program to print numbers from
# 20 to 1 using a while loop.
#
# Example Output:
#
# 20
# 19
# 18
# ...
# 1
# ============================================================

count = 20

while count >= 1:
    print(count)
    count = count - 1