# Program 04 : Print Numbers from 1 to 20
# ============================================================
#
# Problem Statement:
# Write a Python program to print numbers from
# 1 to 20 using a while loop.
#
# Example Output:
#
# 1
# 2
# 3
# ...
# 20
# ============================================================

count = 1

while count <= 20:
    print(count)
    count = count + 1