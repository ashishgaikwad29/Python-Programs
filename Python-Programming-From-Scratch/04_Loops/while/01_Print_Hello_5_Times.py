# ============================================================
# Program 01 : Print Hello 5 Times
# ============================================================
#
# Problem Statement:
# Write a Python program to print the word "Hello"
# exactly 5 times using a while loop.
#
# Example Output:
#
# Hello
# Hello
# Hello
# Hello
# Hello
# ============================================================

count = 1

while count <= 5:
    print("Hello")
    count = count + 1