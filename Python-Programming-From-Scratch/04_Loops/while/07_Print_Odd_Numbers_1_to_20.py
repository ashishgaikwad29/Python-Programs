# Program 07 : Print Odd Numbers from 1 to 20
# ============================================================

# Problem Statement:
# Write a Python program to print all odd numbers
# from 1 to 20 using a while loop.
#
# Example Output:
#
# 1
# 3
# 5
# ...
# 19
# ============================================================

count = 1

while count <= 20:
    print(count)
    count = count + 2